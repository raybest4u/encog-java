package org.encog.mathutil;

public class IntRange {
	private int high;
	private int low;

	public int getHigh() {
		return high;
	}
		
	public IntRange(int high, int low) {
		super();
		this.high = high;
		this.low = low;
	}

	public void setHigh(int high) {
		this.high = high;
	}
	public int getLow() {
		return low;
	}
	public void setLow(int low) {
		this.low = low;
	}
	
	
}
